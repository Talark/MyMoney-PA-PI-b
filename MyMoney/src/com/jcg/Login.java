    //Verifies the login information with the one stored in the database. If they match, the progress bar is initiated
    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {                                          
        
        try {
            
            String sql = "select * from user where username = ? and password = ?";
            
            pst = conn.prepareStatement(sql);
            pst.setString(1, txt_username.getText());
            pst.setString(2, txt_password.getText());
            rs = pst.executeQuery();
            
            if (rs.next()) {
                
                timer.start();
                
                //JOptionPane.showMessageDialog(null, "username and password are Correct");
                
            } else {
                
                JOptionPane.showMessageDialog(null, "username and password are Not Correct");
            }
                    
                   
            
        } catch (Exception e) {
        }
        
        
        
    }                                         

    //Runs the progress bar
    public class progressbar implements ActionListener{
    public void actionPerformed(ActionEvent evt){
    
        int n = jProgressBar1.getValue();
        if (n<100) {
            n++;
            jProgressBar1.setValue(n);
            
        } else {
            
            timer.stop();
            //Admin adm = new Admin();
            TabbedView adm = new TabbedView();
            adm.setVisible(true);
            adm.pack();
            Close();
        }
    
    }
    }
    
