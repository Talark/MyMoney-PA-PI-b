    private void txt_passwordKeyPressed(java.awt.event.KeyEvent evt) {                                        
        
        String username = txt_username.getText();
        
        if (username.equals("")) {
            
            JOptionPane.showMessageDialog(null, "PLEASE ENTER USERNAME FOR LOGIN.");
            
        } else {
            
            if(evt.getKeyCode()==KeyEvent.VK_ENTER){
                
                 try {
            
            String sql = "select * from user where username = ? and password = ?";
            
            pst = conn.prepareStatement(sql);
            pst.setString(1, txt_username.getText());
            pst.setString(2, txt_password.getText());
            rs = pst.executeQuery();
            
            if (rs.next()) {
                
                timer.start();
                
                //JOptionPane.showMessageDialog(null, "username and password are Correct");
                
            } else {
                
                JOptionPane.showMessageDialog(null, "username and password are Not Correct");
            }
                    
                   
            
        } catch (Exception e) {
        }
                
                
            }                               
              
        }
        
    }                                       

    
